package com.hobby.kidult.myhobbyforkidult.data;

public class Ps4Game {

    private String gameName;
    private String gameContition;
    private String gameOpenYN;
    private String gameEndingYN;
    private String gameRentYN;
    private String gamePrice;
    private String gameBuyDate;

    public String getGameName() {
        return gameName;
    }

    public void setGameName(String gameName) {
        this.gameName = gameName;
    }

    public String getGameContition() {
        return gameContition;
    }

    public void setGameContition(String gameContition) {
        this.gameContition = gameContition;
    }

    public String getGameOpenYN() {
        return gameOpenYN;
    }

    public void setGameOpenYN(String gameOpenYN) {
        this.gameOpenYN = gameOpenYN;
    }

    public String getGameEndingYN() {
        return gameEndingYN;
    }

    public void setGameEndingYN(String gameEndingYN) {
        this.gameEndingYN = gameEndingYN;
    }

    public String getGameRentYN() {
        return gameRentYN;
    }

    public void setGameRentYN(String gameRentYN) {
        this.gameRentYN = gameRentYN;
    }

    public String getGamePrice() {
        return gamePrice;
    }

    public void setGamePrice(String gamePrice) {
        this.gamePrice = gamePrice;
    }

    public String getGameBuyDate() {
        return gameBuyDate;
    }

    public void setGameBuyDate(String gameBuyDate) {
        this.gameBuyDate = gameBuyDate;
    }
}
